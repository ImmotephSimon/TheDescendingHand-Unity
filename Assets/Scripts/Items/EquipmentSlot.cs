/// <summary>
/// Defines the physical equipment position an item occupies when equipped.
/// This is not the same as EquipmentCategory.
/// Example: a sword has category Weapon and occupies MainHand.
/// </summary>
public enum EquipmentSlot
{
    MainHand,
    Offhand,
    Helmet,
    Body,
    Gloves,
    Boots,
    Ring,
    Charm
}